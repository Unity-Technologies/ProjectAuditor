namespace Unity.ProjectAuditor.Editor
{
    /// <summary>
    /// Describes the message log level of a code issue
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// An error message
        /// </summary>
        Error,

        /// <summary>
        /// A warning message
        /// </summary>
        Warning,

        /// <summary>
        /// A general information message
        /// </summary>
        Info
    }
}
